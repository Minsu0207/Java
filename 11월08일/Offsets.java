package doit;

public class Offsets {
	private int a;
	private int b;
	
	public Offsets() {
		super();
		this.a = 0;
		this.b = 0;
	}

	public Offsets(int a, int b) {
		super();
		this.a = a;
		this.b = b;
	}

	public int getA() {
		return a;
	}

	public void setA(int a) {
		this.a = a;
	}

	public int getB() {
		return b;
	}

	public void setB(int b) {
		this.b = b;
	}	
}
